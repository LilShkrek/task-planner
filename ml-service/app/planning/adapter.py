def adapt_steps(task, steps):
    task_type = detect_task_type(task)
    if not task_type:
        return steps

    adapted = []
    for index, step in enumerate(steps):
        title, description = _profile_step(task_type, index)
        adapted.append(
            {
                **step,
                "title": title,
                "description": description.format(task_title=task.get("title") or "задача"),
            }
        )
    return adapted


def detect_task_type(task):
    text = _task_text(task)
    for profile in TASK_TYPES:
        if any(_contains_keyword(text, keyword) for keyword in profile["keywords"]):
            return profile
    return None


def _profile_step(task_type, index):
    steps = task_type["steps"]
    if index < len(steps):
        return steps[index]
    return steps[-1]


def _task_text(task):
    return " ".join(str(task.get(field, "")) for field in ("title", "description", "context")).lower()


def _contains_keyword(text, keyword):
    if " " in keyword or "-" in keyword:
        return keyword in text
    return re.search(rf"(^|[^a-zа-яё0-9]){re.escape(keyword)}([^a-zа-яё0-9]|$)", text) is not None
